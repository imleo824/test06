export const matchStageSummary = (() => {
  const totalStages = matchStageRanges.length;
  const deficitStages = matchStageRanges.filter(
    (stage) => stage.waterDiffTotal < 0,
  );
  const surplusStages = matchStageRanges.filter(
    (stage) => stage.waterDiffTotal >= 0,
  );

  const maxVolumeStage = matchStageRanges.reduce(
    (max, s) => (s.volumeTotal > max.volumeTotal ? s : max),
    matchStageRanges[0],
  );
  const maxDeficitStage = matchStageRanges.reduce(
    (max, s) => (s.waterDiffTotal < max.waterDiffTotal ? s : max),
    matchStageRanges[0],
  );

  return {
    totalStages,
    deficitStagesCount: deficitStages.length,
    surplusStagesCount: surplusStages.length,
    maxVolumeStageLabel: maxVolumeStage.label,
    maxVolumeStageVal: maxVolumeStage.volumeTotal.toFixed(1),
    maxDeficitStageLabel: maxDeficitStage.label,
    maxDeficitStageVal: Math.abs(maxDeficitStage.waterDiffTotal).toFixed(1),
  };
})();

