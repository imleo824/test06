export const CustomNestedBar = (props: any) => {
  const { x, y, width, height, payload } = props;
  if (!payload || typeof payload.volume !== "number" || payload.volume <= 0) {
    return null;
  }
  if (
    typeof x !== "number" ||
    typeof y !== "number" ||
    typeof width !== "number" ||
    typeof height !== "number"
  ) {
    return null;
  }

  const yZero = y + height;
  const sf = height / payload.volume;

  const wlValue = payload.winLoss || 0;
  const wlH = Math.abs(wlValue) * sf;

  const wlW = width * 0.6; // Slightly narrower nested bar
  const wlX = x + (width - wlW) / 2;

  let wlY = yZero;
  if (wlValue >= 0) {
    wlY = yZero - wlH;
  } else {
    wlY = yZero;
  }

  const isLoss = wlValue < 0;
  const wlColor = isLoss ? "#e11d48" : "#059669"; // rose-600 vs emerald-600

  return (
    <g>
      {/* Outer Grey Volume bar */}
      <rect
        x={x}
        y={y}
        width={width}
        height={height}
        fill="#cbd5e1"
        fillOpacity={0.6}
        rx={1}
      />
      {/* Nested WinLoss bar (Positive inside, Negative extending below 0) */}
      {wlH > 0 && (
        <rect x={wlX} y={wlY} width={wlW} height={wlH} fill={wlColor} rx={1} />
      )}
    </g>
  );
};

