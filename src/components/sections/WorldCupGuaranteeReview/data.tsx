export const matchTrendSummary = (() => {
  const total = allMatchTrendData.length;
  const positive = allMatchTrendData.filter((row) => row.rate >= 0).length;
  const negative = total - positive;

  const totalVolume = allMatchTrendData.reduce(
    (sum, row) => sum + row.volume,
    0,
  );
  const totalWinLoss = allMatchTrendData.reduce(
    (sum, row) => sum + row.winLoss,
    0,
  );
  const averageHoldRate = (totalWinLoss / totalVolume) * 100;

  const totalExpectedWater = waterDiffMatchData.reduce(
    (sum, row) => sum + row.expectedWater,
    0,
  );
  const totalActualWater = waterDiffMatchData.reduce(
    (sum, row) => sum + row.actualWater,
    0,
  );
  const totalWaterDiff = waterDiffMatchData.reduce(
    (sum, row) => sum + row.waterDiff,
    0,
  );

  // 亏水与超水统计
  const deficitMatches = waterDiffMatchData.filter(
    (row) => row.waterDiff < 0,
  ).length;
  const surplusMatches = waterDiffMatchData.length - deficitMatches;

  // 总亏水钱 (只累加负偏差的绝对值)
  const totalLostWater = waterDiffMatchData
    .filter((row) => row.waterDiff < 0)
    .reduce((sum, row) => sum + Math.abs(row.waterDiff), 0);

  const totalWaterVolume = waterDiffMatchData.reduce((sum, r) => sum + r.volume, 0);
  const expectedWaterRate = (totalExpectedWater / totalWaterVolume) * 100;
  const actualWaterRate = (totalActualWater / totalWaterVolume) * 100;
  const waterRateDiff = actualWaterRate - expectedWaterRate;

  return {
    total,
    positive,
    negative,
    positiveShare: Number(((positive / total) * 100).toFixed(2)),
    negativeShare: Number(((negative / total) * 100).toFixed(2)),
    totalVolume: totalVolume,
    totalWinLoss: totalWinLoss,
    averageHoldRate: Number(((totalWinLoss / totalVolume) * 100).toFixed(2)),
    totalExpectedWater: totalExpectedWater,
    totalActualWater: totalActualWater,
    totalWaterDiff: totalActualWater - totalExpectedWater,
    expectedWaterRate,
    actualWaterRate,
    waterRateDiff,
    deficitMatches,
    surplusMatches,
    deficitShare: Number(((deficitMatches / total) * 100).toFixed(2)),
    surplusShare: Number(((surplusMatches / total) * 100).toFixed(2)),
    totalLostWater: totalLostWater,
  };
})();

